class Venue {
  final String venue_name;
  final String venue_address;
  final String venue_city;
  final String venue_capacity;
  final String venue_surface;

  // Constructeur avec champs obligatoires
  Venue({
    required this.venue_name,
    required this.venue_address,
    required this.venue_city,
    required this.venue_capacity,
    required this.venue_surface,
  });
}
